package de.fhg.fokus.hss;

import java.io.PrintWriter;
import java.io.StringWriter;

import javax.servlet.ServletException;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;

import org.apache.log4j.Logger;
import org.apache.struts.action.ActionForm;
import org.apache.struts.action.ActionForward;
import org.apache.struts.action.ActionMapping;
import org.apache.struts.action.ActionMessage;
import org.apache.struts.action.ExceptionHandler;
import org.apache.struts.config.ExceptionConfig;

/**
 * This is the application's exception handler
 * 
 * @author sur Creation date: 08.06.2005 / 13:31:43
 */
public class HssExceptionHandler extends ExceptionHandler {
	/** logger instance */
	private static Logger LOGGER = Logger.getLogger(HssExceptionHandler.class
			.getName());

	/**
	 * @see org.apache.struts.action.ExceptionHandler#execute(java.lang.Exception,
	 *      org.apache.struts.config.ExceptionConfig,
	 *      org.apache.struts.action.ActionMapping,
	 *      org.apache.struts.action.ActionForm,
	 *      javax.servlet.http.HttpServletRequest,
	 *      javax.servlet.http.HttpServletResponse)
	 */
	public ActionForward execute(Exception ex, ExceptionConfig config,
			ActionMapping mapping, ActionForm form, HttpServletRequest req,
			HttpServletResponse res) throws ServletException {

		// log the exception
		LOGGER.error("exception occured", ex);

		// set the stack trace
		StringWriter sw = new StringWriter();
		PrintWriter pw = new PrintWriter(sw);
		ex.printStackTrace(pw);
		req.setAttribute("exceptionMessage", sw.toString());

		// store the exception for output
		ActionMessage msg = new ActionMessage(config.getKey(), ex.getMessage());
		storeException(req, config.getKey(), msg, new ActionForward(config
				.getPath()), config.getScope());

		return new ActionForward(config.getPath());
	}
}